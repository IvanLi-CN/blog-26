async function generateTestImages() {
  console.log('🖼️ 生成测试图片...');

  const imagesDir = path.resolve('tests/e2e/test-data/images');

  try {
    // 确保目录存在
    await mkdir(imagesDir, { recursive: true });

    // 生成PNG图片
    await writeFile(path.join(imagesDir, 'test-image.png'), PNG_DATA);
    console.log('✅ 生成 test-image.png');

    // 生成JPG图片
    await writeFile(path.join(imagesDir, 'test-image.jpg'), JPEG_DATA);
    console.log('✅ 生成 test-image.jpg');

    // 生成GIF图片
    await writeFile(path.join(imagesDir, 'test-image.gif'), GIF_DATA);
    console.log('✅ 生成 test-image.gif');

    // 生成多个测试图片用于批量上传测试
    await writeFile(path.join(imagesDir, 'test-image-1.png'), PNG_DATA);
    await writeFile(path.join(imagesDir, 'test-image-2.png'), PNG_DATA);
    await writeFile(path.join(imagesDir, 'test-image-3.png'), PNG_DATA);
    console.log('✅ 生成批量测试图片');

    console.log('🎉 测试图片生成完成');
  } catch (error) {
    console.error('❌ 生成测试图片失败:', error);
    throw error;
  }
}

